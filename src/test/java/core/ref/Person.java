package core.ref;

public class Person {
	private int age;
	private int height;
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public Person(int age, int height) {
		super();
		this.age = age;
		this.height = height;
	}
	
	
}
